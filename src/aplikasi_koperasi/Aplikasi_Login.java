    private void prosesLogin(){
        String kodeuser="";
        String password="";
        kodeuser=tkodeuser.getText();
            try {String sql="Select kd_user, nama_user, password, level"
            + " from user where kd_user ='"+kodeuser+"'AND password ='"+jPassword.getText()+"'";
         kon.rs=kon.st.executeQuery(sql);
         if(kon.rs.next()){
             if(tlevel.getText().equals("1")){
                 Aplikasi_Menu_Utama menu = new Aplikasi_Menu_Utama();// Belum ada
                 Aplikasi_Lupa_Password gantipass = new Aplikasi_Lupa_Password();
                 menu.setLocationRelativeTo(null);
                 menu.setVisible(true);
                 menu.NamaUser=kon.rs.getString("nama_user");
                 menu.KodeUser=kon.rs.getString("kd_user");
                 menu.Level=kon.rs.getString("level");
                 gantipass.Kode_User=kon.rs.getString("kd_user");
                 this.dispose();
             }
             else if (tlevel.getText().equals("2")) {
                 Aplikasi_Menu_Utama menu = new Aplikasi_Menu_Utama();//Belum Dibuat
                 Aplikasi_Lupa_Password gantipass = new Aplikasi_Lupa_Password();
                 menu.setLocationRelativeTo(null);
                 menu.setVisible(true);
                 menu.NamaUser=kon.rs.getString("nama_user");
                 menu.KodeUser=kon.rs.getString("kd_user");
                 menu.Level=kon.rs.getString("level");
                 gantipass.Kode_User=kon.rs.getString("kd_user");
                 this.dispose();
             }
             
         }
         else{
             JOptionPane.showMessageDialog(null, "Kode User atau Password Salah");
             bersih();
             tkodeuser.setVisible(true);
             tkodeuser.requestFocus();
         }
         
            
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

