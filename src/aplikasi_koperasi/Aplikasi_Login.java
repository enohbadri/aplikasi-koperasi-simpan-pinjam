    private void tkodeuserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tkodeuserActionPerformed
        // TODO add your handling code here:
        String kodeuser ="";
        kodeuser = tkodeuser.getText();
        try {
            String sql ="Select*from user where kd_user='"+ kodeuser + "'";
            kon.rs = kon.st.executeQuery(sql);
            if (kon.rs.next()) {
                tkodeuser.setEnabled(false);
                tlevel.setText(kon.rs.getString("level"));
                jPassword.setEnabled(true);
                jPassword.requestFocus();
                tkodeuser.setEnabled(false);
            }
            else{
                JOptionPane.showMessageDialog(null, "Kode User Salah");
                bersih();
                tkodeuser.setEnabled(true);
                tkodeuser.requestFocus();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tkodeuserActionPerformed

